          			<h2>About PAWS New England</h2>
                                


                                <p>PAWS New England is a non-profit all breed dog rescue group. PAWS rescues unwanted, abandoned, neglected, and abused dogs from high kill shelters and works to educate the public about the need to spay and neuter pets to prevent overpopulation. We hope you will enjoy this 13 minute video that details our mission, shares some heartwarming stories, and describes the ways in which you can help us!


