          			<p>PAWS New England is comprised of a small group of volunteers from New
					England and Tennessee trying to reduce the euthanization rate
					(previously 90%) at the Tipton County Animal Control shelter in
					Brighton, Tennessee.&nbsp; This shelter is forced to euthanize
					thousands of animals each year due to irresponsible pet owners that do
					not spay or neuter their dogs in the area.  Many of the dogs
					euthanized are dogs that would be consider "highly adoptable" in New
					England: puppies, pure-bred dogs, and dogs that have excellent
					temperaments who are already trained. PAWS tries to bridge the gap
					between hundreds of families in New England who would love to adopt
					these special creatures and the tremendous odds that they face getting
					out of the shelter. Millions of dogs in America are euthanized each
					year due to lack of space and lack of funding to try and find them
					better homes...</p>			
          			<h2>About Tipton County Animal Control</h2>
          			<p>Tipton County Animal Control is a facility with 39 runs for dogs. Regularly, 
					this shelter 60-80 new dogs every week. These numbers are further complicated 
					by an extremely low local adoption rate as well as a dwindling local shelter 
					budget. The animals at Tipton County receive no veterinary care and there are 
					no partnerships with local vets to access low cost spay/neuter programs and cost 
					efficient vaccinations. Rescue groups are quite hesitant to pull from such a 
					shelter because they must assume all the cost of vetting each and every dog. 
					Furthermore, the vast majority of dogs pulled are afflicted with anything from 
					distemper to heartworm. Saving dogs from Tipton County is a costly venture, but 
					PAWS remains committed to this rural shelter who essentially operates without any 
					help. We do our best to facilitate small changes by pulling as many dogs as we have 
					funds to save and on a larger scale by seeking out grant money and local veterinarian 
					volunteers willing to make the vast changes needed to drastically lower this pound's 
					euthanasia rate.</p>
          			<h2>Our Mission</h2>
          			<p>Our mission is to save as many animals as possible, removing them from
					the shelter, having their veterinary needs met prior to traveling and 
					then finding them loving homes throughout New England.  We do this only 
					with the assistance of many people volunteering their time by going to the 
					shelter, transporting the dogs to the vet, fostering dogs who need special care, 
					and arranging for their new and happy homes in this area.  Most importantly, we 
					cannot do any of this without those people willing to open their homes and hearts 
					to these lovely creatures.</p>
					
					
					
					
